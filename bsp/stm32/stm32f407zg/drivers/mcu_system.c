void dal_system_init(void)
{
    HAL_Init();
    SystemClock_Config();
    if (SysTick_Config(SystemCoreClock / TICK_FRQ_HZ))
    {
        /* capture error */
        while (1)
        {
        }
    }
}

void dal_user_init()
{
    dal_uart_init(DAL_UART_1, 230400);
    dal_set_log_uart(DAL_UART_1);
}

